// 1. กำหนด URL ของ API
const url = "https://catfact.ninja/fact";

console.log("กำลังดึงข้อมูลความน่ารักของแมว...");

// 2. ใช้คำสั่ง fetch ไปเอาข้อมูล
fetch(url)
  .then((response) => {
    // 3. เมื่อได้ข้อมูลมา (response) เราแกะกล่องให้อยู่ในรูปแบบ JSON
    return response.json();
  })
  .then((data) => {
    // 4. ได้ข้อมูลที่พร้อมใช้งาน ลองเอามาแสดงผล
    console.log("ข้อมูลที่ได้มาทั้งหมดหน้าตาแบบนี้:", data);
    console.log("ความจริงเกี่ยวกับแมว: " + data.fact);
  })
  .catch((error) => {
    // 5. ถ้าเกิดข้อผิดพลาด เช่น เน็ตหลุด หรือเว็บ API ล่ม จะมาทำงานตรงนี้
    console.error(
      "อ๊ะ! ดึงข้อมูลไม่สำเร็จค่ะ ลองตรวจสอบอินเทอร์เน็ตดูนะคะ",
      error,
    );
  });
